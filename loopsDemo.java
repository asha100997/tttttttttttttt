package FORLOOPS;

public class loopsDemo {
	public static void main(String[] args) {
		System.out.println("===forloop===");
		for(int i=1;i<=5;i++) {
			System.out.println("x");
		}
		//System.out.println();
		System.out.print("====while loop====");
		int i=1;
		while(i<=5) {
			System.out.println(i);
			i++;
		}
	}

}
